/*
*测试标识符
*/

public class Identifer{
			public static void main(String[] arg){
				int $abc=3;
				int $=5;
				int _123=5;
				//    int 123abc=6；标识符不能以数字开头
				//    int abc# =3;   标识符不能包含除了字母、数字、下划线、$之外的其他字符
				//    int class = 3;
				
				System.out.println("$abc,$,_123");
				
				
				}
	}


